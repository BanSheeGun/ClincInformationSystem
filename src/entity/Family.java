package entity;

public class Family {
	private int FamilyId;
	private String Address;
	public int getFamilyId() {
		return FamilyId;
	}
	public void setFamilyId(int familyId) {
		FamilyId = familyId;
	}
	public String getAddress() {
		return Address;
	}
	public void setAddress(String address) {
		Address = address;
	}
}
